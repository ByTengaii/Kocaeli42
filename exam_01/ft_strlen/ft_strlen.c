int		ft_strlen(char *str)
{
	int i = 0;
	while( *(str + i) != '\0')
		i++;	
	return (i);
}

#include <stdio.h>
int	main()
{
	int a = ft_strlen("1234567");
	printf("%d\n",a);
	return (0);
}
